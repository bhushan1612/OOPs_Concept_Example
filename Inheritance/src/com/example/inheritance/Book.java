package com.example.inheritance;

public class Book {

	public void title(String bookName) {
		System.out.println("Book title is : "+bookName);
	}
	
	public void auther(String autherName) {
		System.out.println("Book title is : "+autherName);
	}
	
	public void edition(String editionSeries) {
		System.out.println("Book title is : "+editionSeries);
	}
}
